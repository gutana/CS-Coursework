<h2>
    WELCOME TO POLLMASTER
</h2>
<p>
    Pollmaster is a proprietary, cutting edge software that allows you to
    vote on and submit polls. Using the latest frameworks such as vanilla
    javascript and CSS, the creators of Pollmaster have been able to create
    a buttery-smooth, elegent polling experience.
</p>
<p>
    To get started, you can click on "SIGN UP" in the top right. If you
    are a returning user, you can log in on the left side.
</p>
<p>
    If you'd like to browse and vote without being logged in - that's fine.
    We have made stunning technological breakthroughs to allow you to be able to
    vote on polls without being logged in. You can see recent polls on
    the right side.
</p>
<p>
    <b>PLEASE NOTE: </b> do NOT use any passwords that you have used elsewhere. While PollMaster 
    does NOT store user passwords unencrypted, the The CS student web server does not
    have an SSL certificate which means, when you send your password to us,
    it is transmitted as PLAIN TEXT. Please use passwords such that if the password
    were to be stolen, you would not mind. 
</p>